# Arithmetic Operators
a = 10
b = 5
addition = a + b
subtraction = a - b
multiplication = a * b
division = a / b
floor_division = a // b
modulus = a % b
exponentiation = a ** b

print(f"Addition: {addition}")
print(f"Subtraction: {subtraction}")
print(f"Multiplication: {multiplication}")
print(f"Division: {division}")
print(f"Floor Division: {floor_division}")
print(f"Modulus: {modulus}")
print(f"Exponentiation: {exponentiation}")

# Comparison Operators
x = 10
y = 20
print(f"\nComparison Operators:")
print(f"x == y: {x == y}")
print(f"x != y: {x != y}")
print(f"x < y: {x < y}")
print(f"x > y: {x > y}")
print(f"x <= y: {x <= y}")
print(f"x >= y: {x >= y}")

# Logical Operators
p = True
q = False
print(f"\nLogical Operators:")
print(f"p and q: {p and q}")
print(f"p or q: {p or q}")
print(f"not p: {not p}")

# Assignment Operators
c = 15
print(f"\nAssignment Operators:")
c += 5
print(f"c += 5: {c}")
c -= 3
print(f"c -= 3: {c}")
c *= 2
print(f"c *= 2: {c}")
c /= 4
print(f"c /= 4: {c}")
c //= 2
print(f"c //= 2: {c}")
c **= 3
print(f"c **= 3: {c}")

# Bitwise Operators
m = 7  # 0111 in binary
n = 3  # 0011 in binary
print(f"\nBitwise Operators:")
bitwise_and = m & n
bitwise_or = m | n
bitwise_xor = m ^ n
bitwise_not_m = ~m
bitwise_left_shift = m << 1
bitwise_right_shift = m >> 1

print(f"Bitwise AND: {bitwise_and}")
print(f"Bitwise OR: {bitwise_or}")
print(f"Bitwise XOR: {bitwise_xor}")
print(f"Bitwise NOT of m: {bitwise_not_m}")
print(f"Bitwise Left Shift of m: {bitwise_left_shift}")
print(f"Bitwise Right Shift of m: {bitwise_right_shift}")
